#include "GPS_Sensor.h"

void GPS_Sensor::setPos(double E, double F, double G)
{
    antennaPos[0] = E;
    antennaPos[1] = F;
    antennaPos[2] = G;
}
//-----------------------------------------------------------------------------------------------//
//-----------------------------------------------------------------------------------------------//
double GPS_Sensor::getPosE()
{
    return antennaPos[0];
}
//-----------------------------------------------------------------------------------------------//
//-----------------------------------------------------------------------------------------------//
double GPS_Sensor::getPosF()
{
    return antennaPos[1];
}
//-----------------------------------------------------------------------------------------------//
//-----------------------------------------------------------------------------------------------//
double GPS_Sensor::getPosG()
{
    return antennaPos[2];
}
